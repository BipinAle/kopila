package com.mobile.kopila.pojo;

public class SahayogSamparka {
    String name,occupation,number,adderss;

    public String getName() {
        return name;
    }

    public String getOccupation() {
        return occupation;
    }

    public String getNumber() {
        return number;
    }

    public String getAdderss() {
        return adderss;
    }

    public SahayogSamparka(String name, String occupation, String number, String adderss) {
        this.name = name;
        this.occupation = occupation;
        this.number = number;
        this.adderss = adderss;
    }
}
